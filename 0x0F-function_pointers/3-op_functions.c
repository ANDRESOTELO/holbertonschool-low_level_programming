#include "3-calc.h"

/**
 *op_add - Funtion to add
 *@a: Param Integer
 *@b: Param Integer
 *Return: Integer of sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *op_sub - Funtion to substract
 *@a: Param Integer
 *@b: Param Integer
 *Return: Integer of sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *op_mul - Funtion to multiplies
 *@a: Param Integer
 *@b: Param Integer
 *Return: Integer of mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - Funtion to div
 *@a: Param Integer
 *@b: Param Integer
 *Return: Integer of div
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - Funtion to mod
 *@a: Param Integer
 *@b: Param Integer
 *Return: Integer of mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
